    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $notification = new Notification();
        $notification->notification_date = $request->notification_date;
        $notification->notification_text = $request->notification_text;
        $notification->lawyer_id = $request->lawyer_id;
        $notification->notification_status = $request->notification_status;
        $notification->created_by = $request->notification_date;
        if ($notification->save()) {
            return response()->json(['success' => true]);

        } else
            return response()->json(['success' => false]);

    }

